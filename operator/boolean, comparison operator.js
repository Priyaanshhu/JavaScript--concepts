// BOOLEAN AND COMPARISON OPERATOR 

// Booleans (true, false)
let num1 = 7;
let num2 = "7";
console.log(num1<num2);

// Comparison ( == vs === )
// '==' this will only check value not data type
console.log(num1 == num2);
// '===' this will check value and and data type
console.log(num1 === num2);

// != vs !==
console.log(num1 != num2);
console.log(num1 !== num2);
