// TERNARY OPERATOR
// ternary operator is a shorthand way to write conditonal operator

// if else code :
var age = 4;
var drink;

if (age >= 5) {
  drink = "coffee";
} else {
  drink = "milk";
}
console.log(drink);


// same code by using ternary operator / conditional operator 
var age = 3;
var drink = age >= 5 ? "coffee" : "milk";
console.log(drink);