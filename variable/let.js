// LET
// let allows you to declare variables that are limited to a specific block of  code, making it easier to manage scope and avoid naming conflicts.

// - Declares a block-scoped variable
// - Can be reassigned
// - Cannot be redeclared in the same scope
// - Not hoisted (i.e., must be declared before use)

// declare variable with let  
let fruitName = "apple";
fruitName = "mango";
console.log(fruitName);


