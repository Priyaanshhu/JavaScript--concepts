// CONSTANT
// const allows you to declare variables that cannot be changed once they are set, helping to prevent unintended changes to important values.

// - Declares a block-scoped constant
// - Cannot be reassigned
// - Cannot be redeclared in the same scope
// - Not hoisted (i.e., must be declared before use)
// - Must be assigned a value when declared

const fruitName = "pomegranate";
console.log(fruitName);