// BREAK KEYWORD
// "break" exits the loop or switch statement entirely.

for(let i = 1; i<=10; i++){
    if(i===4){
        break;
    }
    console.log(i);
}


// CONTINUE KEYWORD 
// "continue" skips the current iteration and moves on to the next one.

for(let i = 1; i<=10; i++){
    if(i===4){
        continue;
    }
    console.log(i);
}
