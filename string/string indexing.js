// STRING
// string is a sequence of characters, such as letters, numbers, or symbols, that are stored and manipulated as a single entity. Strings can be thought of as a collection of characters, like a word or a sentence.
// Example: "Hello it's Me" is a string.


// STRING INDEXING 
// String indexing refers to the process of accessing individual characters within a string using their position or index. Each character in a string has a unique index, starting from 0.

let firstName = "monika";
//  m   o   n   i   k   a 
//  0   1   2   3   4   5 
console.log(firstName[0]);
console.log(firstName[1]);
console.log(firstName[2]);
console.log(firstName[3]);
console.log(firstName[4]);
console.log(firstName[5]);

// length of string 
console.log(firstName.length);

// last Index : length - 1 
console.log(firstName[firstName.length - 1]);
console.log(firstName[firstName.length - 2]);
console.log(firstName[firstName.length - 3]);
console.log(firstName[firstName.length - 4]);
console.log(firstName[firstName.length - 5]);
console.log(firstName[firstName.length - 6]);