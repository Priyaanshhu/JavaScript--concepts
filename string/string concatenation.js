// STRING CONCATENATION

// String concatenation is the process of combining two or more strings into a single string. It involves joining multiple strings together, end-to-end, to form a new string.

// Example 1
let string_a = "Hello";
let string_b = "I'm a guy";

let newString_ = (string_a) + " " + (string_b);
console.log(typeof newString_, newString_);

// Example 2
let string1 = "17";
let string2 = "10";

let newString = Number(string1) + Number(string2);
